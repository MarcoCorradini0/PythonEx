# ESERCIZIO 1: Calcoli Base
# Obiettivo: Praticare tutti gli operatori aritmetici
# Note:
# - La divisione (/) restituisce SEMPRE un float, anche se divisibile esattamente
# - La divisione intera (//) arrotonda verso il basso (floor)
# - Il modulo (%) restituisce il resto della divisione intera

# Definire i valori
a = 15
b = 4

# Eseguire le operazioni
print(f"{a} + {b} = {a + b}")
print(f"{a} - {b} = {a - b}")
print(f"{a} * {b} = {a * b}")
print(f"{a} / {b} = {a / b}")
print(f"{a} // {b} = {a // b}")
print(f"{a} % {b} = {a % b}")
print(f"{a} ** {b} = {a ** b}")